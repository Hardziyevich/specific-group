package com.github.akarazhev.jacademy.jprog.basics;

public class BlastOff {

    public static void main(String[] args) {
        int i = 10;
        while (i > 0) {
            System.out.println(i);
            i--;
        }

        System.out.println("Blast off!");

        for (int j = 10; j > 0; j--) {
            System.out.println(j);
        }

        System.out.println("Blast off!");
    }
}
